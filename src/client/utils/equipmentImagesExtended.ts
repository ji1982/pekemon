// 默认图片（当找不到具体装备图片时使用）
export const DEFAULT_EQUIPMENT_IMAGE = 'https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/items/rare-candy.png';

// 根据装备名称获取图片URL
export const getEquipmentImageByName = (equipmentName: string): string => {
  return EQUIPMENT_IMAGES_EXTENDED[equipmentName] || DEFAULT_EQUIPMENT_IMAGE;
};

// 根据装备槽位、稀有度和属性类型获取图片URL
export const getEquipmentImageByType = (slot: string, rarity: string, typeAffinity?: string): string => {
  if (typeAffinity && TYPE_EQUIPMENT_IMAGES[slot]?.[typeAffinity]) {
    return TYPE_EQUIPMENT_IMAGES[slot][typeAffinity];
  }
  
  // 如果没有特定属性，返回对应槽位的默认图片
  const slotDefaults: Record<string, string> = {
    Weapon: 'https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/items/upgrade.png',
    Armor: 'https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/items/metal-coat.png',
    Accessory: 'https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/items/silk-scarf.png'
  };
  
  return slotDefaults[slot] || DEFAULT_EQUIPMENT_IMAGE;
};